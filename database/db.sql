DROP TABLE IF EXISTS detalle_compra CASCADE;
DROP TABLE IF EXISTS compra CASCADE;
DROP TABLE IF EXISTS empleado_contraseña CASCADE;
DROP TABLE IF EXISTS cliente_nit CASCADE;
DROP TABLE IF EXISTS producto CASCADE;
DROP TABLE IF EXISTS proveedor CASCADE;
DROP TABLE IF EXISTS categoria CASCADE;
DROP TABLE IF EXISTS usuario CASCADE;
DROP TABLE IF EXISTS rol CASCADE;

CREATE TABLE rol (
    id_rol INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre_rol VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE usuario (
    id_usuario INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_rol INTEGER NOT NULL,
    nombre VARCHAR(120) NOT NULL,
    correo VARCHAR(120) NOT NULL UNIQUE,
    numero VARCHAR(20) NOT NULL,
    activo BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT fk_usuario_rol
        FOREIGN KEY (id_rol) REFERENCES rol(id_rol)
);

CREATE TABLE categoria (
    id_categoria INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre_categoria VARCHAR(100) NOT NULL UNIQUE,
    descripcion TEXT NOT NULL
);

CREATE TABLE proveedor (
    id_proveedor INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre_proveedor VARCHAR(120) NOT NULL,
    correo VARCHAR(120) NOT NULL UNIQUE,
    numero VARCHAR(20) NOT NULL
);

CREATE TABLE producto (
    id_producto INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre_producto VARCHAR(140) NOT NULL,
    id_categoria INTEGER NOT NULL,
    id_proveedor INTEGER NOT NULL,
    grupo_kpop VARCHAR(80) NOT NULL,
    precio NUMERIC(10, 2) NOT NULL,
    stock INTEGER NOT NULL,
    descripcion TEXT NOT NULL,
    CONSTRAINT fk_producto_categoria
        FOREIGN KEY (id_categoria) REFERENCES categoria(id_categoria),
    CONSTRAINT fk_producto_proveedor
        FOREIGN KEY (id_proveedor) REFERENCES proveedor(id_proveedor),
    CONSTRAINT chk_producto_precio CHECK (precio >= 0),
    CONSTRAINT chk_producto_stock CHECK (stock >= 0)
);

CREATE TABLE empleado_contraseña (
    id_empleado INTEGER PRIMARY KEY,
    password_hash VARCHAR(64) NOT NULL,
    CONSTRAINT fk_empleado_usuario
        FOREIGN KEY (id_empleado) REFERENCES usuario(id_usuario) ON DELETE CASCADE
);

CREATE TABLE cliente_nit (
    id_cliente INTEGER PRIMARY KEY,
    nit VARCHAR(20) UNIQUE,
    CONSTRAINT fk_cliente_usuario
        FOREIGN KEY (id_cliente) REFERENCES usuario(id_usuario)
);

CREATE TABLE compra (
    id_compra INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_cliente INTEGER NOT NULL,
    id_empleado INTEGER NOT NULL,
    fecha_compra TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    metodo_pago VARCHAR(30) NOT NULL,
    estado VARCHAR(20) NOT NULL DEFAULT 'pagada',
    CONSTRAINT fk_compra_cliente
        FOREIGN KEY (id_cliente) REFERENCES usuario(id_usuario),
    CONSTRAINT fk_compra_empleado
        FOREIGN KEY (id_empleado) REFERENCES usuario(id_usuario),
    CONSTRAINT chk_compra_estado CHECK (estado IN ('pagada', 'anulada', 'pendiente'))
);

CREATE TABLE detalle_compra (
    id_compra INTEGER NOT NULL,
    id_producto INTEGER NOT NULL,
    cantidad INTEGER NOT NULL,
    precio_unitario NUMERIC(10, 2) NOT NULL,
    PRIMARY KEY (id_compra, id_producto),
    CONSTRAINT fk_detalle_compra
        FOREIGN KEY (id_compra) REFERENCES compra(id_compra) ON DELETE CASCADE,
    CONSTRAINT fk_detalle_producto
        FOREIGN KEY (id_producto) REFERENCES producto(id_producto),
    CONSTRAINT chk_detalle_cantidad CHECK (cantidad > 0),
    CONSTRAINT chk_detalle_precio CHECK (precio_unitario >= 0)
);

CREATE INDEX idx_usuario_correo ON usuario(correo);
CREATE INDEX idx_producto_categoria ON producto(id_categoria);
CREATE INDEX idx_producto_proveedor ON producto(id_proveedor);
CREATE INDEX idx_compra_fecha ON compra(fecha_compra);
CREATE INDEX idx_detalle_producto ON detalle_compra(id_producto);

CREATE VIEW vista_resumen_ventas AS
SELECT
    c.id_compra,
    c.fecha_compra,
    cliente.nombre AS cliente,
    empleado.nombre AS empleado,
    c.metodo_pago,
    c.estado,
    COUNT(dc.id_producto) AS productos_distintos,
    SUM(dc.cantidad) AS unidades,
    SUM(dc.cantidad * dc.precio_unitario) AS total
FROM compra c
JOIN usuario cliente ON cliente.id_usuario = c.id_cliente
JOIN usuario empleado ON empleado.id_usuario = c.id_empleado
JOIN detalle_compra dc ON dc.id_compra = c.id_compra
GROUP BY c.id_compra, c.fecha_compra, cliente.nombre, empleado.nombre, c.metodo_pago, c.estado;

