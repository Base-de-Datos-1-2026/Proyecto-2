INSERT INTO detalle_compra (id_compra, id_producto, cantidad, precio_unitario)
SELECT
    c.id_compra,
    p.id_producto,
    ((c.id_compra + linea.linea) % 3) + 1,
    p.precio
FROM compra c
CROSS JOIN generate_series(1, 3) AS linea(linea)
JOIN producto p
    ON p.id_producto = ((c.id_compra * 3 + linea.linea - 1) % 50) + 1;

